SELECT ROWNUM,A.CUSTOMER_ID,A.NAME,MAX(CREDIT_LIMIT),A.SALESMAN_ID FROM
(SELECT C.*,O.ORDER_ID,O.STATUS,O.SALESMAN_ID,O.ORDER_DATE
FROM CUSTOMERS C, ORDERS O
WHERE C.CUSTOMER_ID = O.CUSTOMER_ID) A
GROUP BY A.CUSTOMER_ID,A.NAME,A.SALESMAN_ID,ROWNUM
ORDER BY A.CUSTOMER_ID,ROWNUM;


