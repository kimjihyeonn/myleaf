
SELECT ROWNUM,C.*,O.STATUS
FROM CUSTOMERS C,ORDERS O
WHERE C.CUSTOMER_ID=O.CUSTOMER_ID;


SELECT D.PRODUCT_ID,D.PRODUCT_NAME,D.WAREHOUSE_NAME,SUM(D.QUANTITY),D.LOCATION_ID
FROM(
SELECT P.*,I.QUANTITY,W.*
FROM PRODUCTS P,INVENTORIES I, WAREHOUSES W
WHERE P.PRODUCT_ID = I.PRODUCT_ID AND I.WAREHOUSE_ID= W.WAREHOUSE_ID
 
ORDER BY P.PRODUCT_ID) D
GROUP BY D.WAREHOUSE_NAME,D.LOCATION_ID,D.PRODUCT_ID,D.PRODUCT_NAME
ORDER BY D.PRODUCT_ID
;


SELECT D.* FROM(
SELECT P.*,I.QUANTITY,W.*
FROM PRODUCTS P,INVENTORIES I, WAREHOUSES W
WHERE P.PRODUCT_ID = I.PRODUCT_ID AND I.WAREHOUSE_ID= W.WAREHOUSE_ID
ORDER BY P.PRODUCT_ID) D
WHERE  D.wareHOUSE_ID=5 AND D.PRODUCT_ID=4;
